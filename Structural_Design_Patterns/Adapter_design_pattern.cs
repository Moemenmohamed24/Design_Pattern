:التعريف الدقيق
Adapter Pattern هو نمط تصميم هيكلي (Structural Design Pattern) بيسمح لكلاسات ذات interfaces غير متوافقة إنها تشتغل مع بعضها عن طريق إنشاء "محول" أو "wrapper" بيترجم interface واحد لآخر.
التشبيه من الحياة الواقعية:
زي محول الكهربا لما تسافر - عندك شاحن أمريكي (قابس بفتحتين مسطحتين) ومحتاج توصله في مقبس أوروبي (دائري). المحول هو الـ Adapter اللي بيخلي الاتنين يشتغلوا مع بعض بدون ما تغير في الشاحن أو المقبس.



  
